String.prototype.applyRules=function(arrayRules){//array containing rules to be applied
	var res=this;
	var i;
	for(i=0; i<arrayRules.length; i++){
		var rule=arrayRules[i];
		var from=new RegExp(rule.split("->")[0],"g");//a regular expression
		var to=rule.split("->")[1];
		res=res.replace(from,to);
	}
	return(res);
}

