String.prototype.guessMaleName=function(){//"gr".guessMaleName gives Guru ; it's done in the order of most frequent to least frequent
	if(/[aeiou]/.test(this)){
		return(this);//contains vowels
	}
	var regex=new RegExp("^"+this.split("").join(".*")+".*");
	var i;
	for(i=0;i<freqnt_malenames.length;i++){
		if(regex.test(freqnt_malenames[i])){
			return(freqnt_malenames[i]);
		}
	}
	return(this);	
}
String.prototype.guessFemaleName=function(){//"gr".guessMaleName gives Guru ; it's done in the order of most frequent to least frequent
	if(/[aeiou]/.test(this)){
		return(this);//contains vowels
	}
	var regex=new RegExp("^"+this.split("").join(".*")+".*");
	var i;
	for(i=0;i<freqnt_femalenames.length;i++){
		if(regex.test(freqnt_femalenames[i])){
			return(freqnt_femalenames[i]);
		}
	}
	return(this);	
}
